class Car {
	
	private String name;
	private int countTire;
	
	
	public Car(String name, int countTire) {
		super();
		this.name = name;
		this.countTire = countTire;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getCountTire() {
		return countTire;
	}


	public void setCountTire(int countTire) {
		this.countTire = countTire;
	}
	
	

}